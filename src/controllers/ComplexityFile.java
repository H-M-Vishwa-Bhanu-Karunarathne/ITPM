package controllers;

public class ComplexityFile {
	
	String fileName;
	int loc;
	int totComplexity;
	
	
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public int getLoc() {
		return loc;
	}
	public void setLoc(int loc) {
		this.loc = loc;
	}
	public int getTotComplexity() {
		return totComplexity;
	}
	public void setTotComplexity(int totComplexity) {
		this.totComplexity = totComplexity;
	}
	
	
	
}
